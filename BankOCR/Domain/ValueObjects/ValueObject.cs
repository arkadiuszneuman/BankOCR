using System.Collections.Generic;
using System.Linq;

namespace BankOCR.Domain.ValueObjects
{
    // Some parts of this code has been taken from https://enterprisecraftsmanship.com/posts/value-object-better-implementation/
    public abstract class ValueObject
    {
        protected virtual IEnumerable<object> GetEqualityComponents()
        {
            return GetType().GetProperties()
                .Select(propertyInfo => propertyInfo.GetValue(this));
        }

        public override bool Equals(object obj)
        {
            if (obj == null)
                return false;

            if (GetType() != obj.GetType())
                return false;

            var valueObject = (ValueObject)obj;

            return GetEqualityComponents().SequenceEqual(valueObject.GetEqualityComponents());
        }

        public override int GetHashCode()
        {
            return GetEqualityComponents()
                .Aggregate(1, (current, obj) =>
                {
                    unchecked
                    {
                        return current * 23 + (obj?.GetHashCode() ?? 0);
                    }
                });
        }

        public static bool operator ==(ValueObject a, ValueObject b)
        {
            if (ReferenceEquals(a, null) && ReferenceEquals(b, null))
                return true;

            if (ReferenceEquals(a, null) || ReferenceEquals(b, null))
                return false;

            return a.Equals(b);
        }

        public static bool operator !=(ValueObject a, ValueObject b)
        {
            return !(a == b);
        }
    }
}